package leetcode4;

public class largest {
	public static int largest(int[] arr) {

		int largest = arr[0];

		for (int i = 0; i < arr.length; i++) {
			if (largest < arr[i]) {
				largest = arr[i];
			}

		}
		return largest;

	}

	public static void main(String[] args) {
		int[] a = { 10, 5, 25, 8, 3 };
		System.out.println(largest(a));

	}

}
